package de.ostfalia.application.data.entity;

public class BlinkSettings {

    private int blinkCount;
    private long blinkDuration;

    public int getBlinkCount() {
        return blinkCount;
    }

    public void setBlinkCount(int blinkCount) {
        this.blinkCount = blinkCount;
    }

    public long getBlinkDuration() {
        return blinkDuration;
    }

    public void setBlinkDuration(long blinkDuration) {
        this.blinkDuration = blinkDuration;
    }
}
